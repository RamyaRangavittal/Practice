package practice;

public class SearchNumberInArray {

	public static void main(String[] args) {
		int a[]= {4,5,6,8,7};
		int x=7;
		boolean found= false;
		
		for (int i=0;i<a.length;i++) {
			if(a[i]==x) {
				found=true;
				break;
			}}
				if(found)
				System.out.println(x+"is found");
				
			
			else
			
				System.out.println("Number is not available in given array ");
				
			
		}}
		
	

